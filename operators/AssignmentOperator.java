public class AssignmentOperator {
    public static void main(String[] args) {
        // = EQUAL TO Operator
        int a = 10, b = 15;
        System.out.println(a);// 10
        // += EQUAL TO Operator
        a += b;
        System.out.println(a);// 25
        // -= EQUAL TO Operator
        b -= 10;
        System.out.println(b);// 5
        // *= EQUAL TO Operator
        a *= 2;
        System.out.println(a);// 50
        // /= EQUAL TO Operator
        b /= 5;
        System.out.println(b);// 1
    }
}
