public class Library_methods {
    public static void main(String[] args) {
        System.out.println(Math.sqrt(24));
        System.out.println(Math.sqrt(16));

        System.out.println(Math.floor(5.3));// Gives the Round of this number (-)
        System.out.println(Math.floor(5.7));

        System.out.println(Math.ceil(6.9));// Gives the Round of this number (+)
        System.out.println(Math.ceil(5.5));
    }
}
